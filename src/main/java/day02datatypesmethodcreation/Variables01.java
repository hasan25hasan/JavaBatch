package day02datatypesmethodcreation;

public class Variables01 {

    public static void main(String[] args) {
        //Ornek 1: Sehir ismi icin  bir variable oluşturun ve once "Ankara" sonrada "İzmir" değeri atayıp ekrana yazdırın
        String cityName = "Ankara";
        System.out.println(cityName);

        cityName ="İzmir";
        System.out.println(cityName);

        //Ornek 2: Sehir plaka kodu için bir variable oluşturun ve once "06" sonra da "35" değeri atayıp ekrana yazdırın
        byte plakaKodu =06;
        System.out.println(plakaKodu);

        plakaKodu = 35;
        System.out.println(plakaKodu);

        //Ornek 3: Basarılı mı? sorusu için bir variable oluşturun ve önce "false" sonrada "true" değeri atayıp ekrana yazdırın.
        boolean isSuccessful = false;
        System.out.println(isSuccessful);

        isSuccessful= true;
        System.out.println(isSuccessful);














    }




}
